public class Calculator {

    public Integer add(Integer a, Integer b) {
        return a + b;
    }

    public Double add(Double a, Double b) {
        return a + b;
    }

    public Long add(Long a, Long b) {
        return a + b;
    }

    public Float add(Float a, Float b) {
        return a + b;
    }
}
